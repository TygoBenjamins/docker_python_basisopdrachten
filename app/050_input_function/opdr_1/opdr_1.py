# Hier komt je code, maak gebruik van de input functie om de lengte van de rechthoekzijden van de driehoek op te vragen.

lengte1 = float(input("Voer de lengte van de eerste zijde in: "))
lengte2 = float(input("Voer de lengte van de tweede zijde in: "))
lengte3 = (lengte1**2 + lengte2**2)**0.5
print(f"De lengte van de eerste zijde is {lengte1} en van de tweede zijde is {lengte2}")
print(f"De lengte van de derde zijde is {lengte3}")